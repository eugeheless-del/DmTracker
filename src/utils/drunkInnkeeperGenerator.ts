/**
 * Current moods and states
 */
const MOODS = [
  'drunk' as const,
  'paranoid' as const,
  'nostalgic' as const,
  'aggressive' as const
];

/**
 * Get random element from array
 */
const getRandomElement = <T,>(array: T[]): T => {
  return array[Math.floor(Math.random() * array.length)];
};

/**
 * Generate random UUID-like ID
 */
const generateId = (): string => {
  return Date.now().toString(36) + Math.random().toString(36).substr(2);
};

/**
 * Generate a quirky NPC with all randomized attributes
 * @returns QuirkyNPC object with randomized characteristics
 */
export const generateQuirkyNPC = (): QuirkyNPC => {
  return {
    id: generateId(),
    name: getRandomElement(QUIRKY_NAMES),
    background: getRandomElement(BACKGROUNDS),
    physicalQuirk: getRandomElement(PHYSICAL_QUIRKS),
    smell: getRandomElement(SMELLS),
    speechPattern: getRandomElement(SPEECH_PATTERNS),
    secretTrade: getRandomElement(SECRET_TRADES),
    lyingAbout: getRandomElement(LIES_ABOUT),
    mood: getRandomElement(MOODS)
  };
};

/**
 * Format NPC description into a readable human-friendly text
 * @param npc QuirkyNPC object to format
 * @returns Formatted description string
 */
export const formatNPCDescription = (npc: QuirkyNPC): string => {
  const moodText = {
    'drunk': 'пьян как чугун',
    'paranoid': 'видит врагов повсюду',
    'nostalgic': 'погружен в воспоминания',
    'aggressive': 'готов к драке'
  };

  return (
    `Бывший ${npc.background}, ` +
    `пахнет ${npc.smell}. ` +
    `${npc.physicalQuirk}. ` +
    `${npc.speechPattern}. ` +
    `Торгует ${npc.secretTrade}, ` +
    `но врёт про ${npc.lyingAbout}. ` +
    `Сейчас ${moodText[npc.mood]}.`
  );
};

/**
 * Generate multiple unique quirky NPCs
 * @param count Number of NPCs to generate
 * @returns Array of QuirkyNPC objects
 */
export const generateQuirkyNPCs = (count: number): QuirkyNPC[] => {
  return Array.from({ length: count }, () => generateQuirkyNPC());
};
